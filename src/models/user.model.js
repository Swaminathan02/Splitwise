const { DataTypes } = require("sequelize");
const { sequelize } = require("../config/db");

const User = sequelize.define(
  "User",
  {
    id: {
      type: DataTypes.INTEGER,
      autoIncrement: true,
      primaryKey: true,
    },

    email: {
      type: DataTypes.STRING,
      allowNull: false,
      unique: true,
      validate: {
        isEmail: true,
      },
    },

    password: {
      type: DataTypes.STRING,
      allowNull: false,
    },

    defaultCurrency: {
      type: DataTypes.STRING(10),
      allowNull: false,
      defaultValue: "INR",
    },
  },
  {
    tableName: "users",
    timestamps: true,
  }
);

module.exports = User;